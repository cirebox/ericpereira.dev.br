  static String translate(String key, BuildContext context) {
    final Locale locale = Localizations.localeOf(context);
    final String languageCode = locale.languageCode;

    if (_translations.containsKey(languageCode) &&
        _translations[languageCode]!.containsKey(key)) {
      return _translations[languageCode]![key]!;
    }

    // Fallback para português se a chave não for encontrada
    if (_translations['pt']!.containsKey(key)) {
      return _translations['pt']![key]!;
    }

    return key; // Retorna a própria chave se não encontrar a tradução
  }
}

// Classe para acessar as traduções de forma mais simples
class Translate {
  static String text(String key, BuildContext context) {
    return AppTranslations.translate(key, context);
  }
}
